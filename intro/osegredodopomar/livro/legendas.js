function iniciarSincronizacao(audioElement, slideIndex) {
  

  const displayTexto = document.getElementById(`legenda${slideIndex}`);
  const mapa = scriptsDasHistorias[slideIndex];

  if (!mapa || !displayTexto) return;

  let ultimoTexto = null;

  audioElement.ontimeupdate = () => {

    const tempoAtual = audioElement.currentTime;

    const fraseAtual = [...mapa]
      .reverse()
      .find(p => tempoAtual >= p.tempo);

    const novoTexto = fraseAtual
      ? JSON.stringify(fraseAtual.texto)
      : "";

    if (novoTexto !== ultimoTexto) {

      ultimoTexto = novoTexto;

      if (fraseAtual && fraseAtual.texto) {

        displayTexto.classList.remove(
          "legenda-top",
          "legenda-bottom",
          "legenda-middle"
        );

        if (fraseAtual.posicao === "top") {
          displayTexto.classList.add("legenda-top");
        }

        if (fraseAtual.posicao === "bottom") {
          displayTexto.classList.add("legenda-bottom");
        }

        if (fraseAtual.posicao === "middle") {
          displayTexto.classList.add("legenda-middle");
        }

        animarPalavras(fraseAtual.texto, displayTexto);

        requestAnimationFrame(() => {
          displayTexto.style.opacity = "1";
        });

      } else {

        displayTexto.style.opacity = "0";

        setTimeout(() => {
          displayTexto.innerHTML = "";
        }, 2500);
      }
    }
  };
}

function animarPalavras(palavras, displayTexto) {

  displayTexto.innerHTML = palavras
    .map(p => `<span>${p.palavra}</span>`)
    .join(" ");

  const spans = displayTexto.querySelectorAll("span");

  let index = 0;

  function proximaPalavra() {

    if (index > 0) {
      spans[index - 1].classList.remove("palavra-ativa");
    }

    if (index < spans.length) {

      spans[index].classList.add("palavra-ativa");

      const tempoAtual = palavras[index].tempo;

      index++;

      setTimeout(proximaPalavra, tempoAtual);

    } else {

      spans.forEach(s => {
        s.classList.remove("palavra-ativa");
      });
    }
  }

  proximaPalavra();
}